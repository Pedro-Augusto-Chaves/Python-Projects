print("Your mission is to find the treasure.")

direction = input("You have two passages on the way, you go left or right? ").lower()

if direction != 'left':
    print("You found a hungry bear. Game Over.")
else:
    swim_or_wait = input("You ended up facing a river. Swim or wait for a boat? ").lower()

    if swim_or_wait != 'wait':
        print("The current was very strong, you drowned. Game Over.")
    else:
        door_color = input("Choose between 3 doors: Yellow, Blue or Red. ").lower()
        
        if door_color == 'red':
            print("You got burned. Game Over.")
        elif door_color == 'blue':
            print('You got hit by a lightning. Game Over.')
        elif door_color == "yellow":
            print("You win!")
        else:
            print("Game Over.")
    
    
